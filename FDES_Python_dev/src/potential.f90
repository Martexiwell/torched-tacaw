  subroutine bw_limit(dat) ! performs bandwidth limiting for **complex** arrays, it assumes that they are in real-space

    use fftw

    implicit none

    double complex, intent(in) :: dat(:,:)

    call fft2d(dat,bwlim)
    bwlim = bwlim * bw_mask
    call bfft2d(bwlim,dat)

  end subroutine bw_limit

