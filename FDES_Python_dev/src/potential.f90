  !subroutine build_Sk(S_k, S_inv, Nx, Ny, p, method, reg_param, r2c)
  !--------------------------------------------------------------------------------------------------------------------------
  subroutine build_Sk
    
    use global!, only : ntx,nty
    use atoms!, only : px,py
    use fftw, only : getkx, getky

    implicit none
    
    !integer, intent(in) :: Nx, Ny
    !real(dp), intent(in) :: p
    !character(len=*), intent(in), optional :: method
    !real(dp), intent(in), optional :: reg_param
    !logical, intent(in), optional :: r2c

    !real(dp), intent(out), allocatable :: S_k(:,:)
    !real(dp), intent(out), allocatable :: S_inv(:,:)

    character(len=10) :: mth
    double precision :: reg
    !logical :: half
    integer :: ix, iy
    !integer :: jmax
    !double precision :: argx, argy, val, p2
    double precision :: kx, ky, tmp, p2
    double precision :: maxS

    ! defaults
    mth = "threshold"
    !mth = "wiener"
    !mth = "unit"
    !if (present(method)) mth = trim(method)
    !reg = -1.0_dp
    !if (present(reg_param)) reg = reg_param
    !half = .false.
    !if (present(r2c)) half = r2c

    !if (half) then
      !jmax = Ny/2 + 1
      !allocate(S_k(Nx, jmax))
      !allocate(S_inv(Nx, jmax))
    !else
      !jmax = Ny
      !allocate(S_k(Nx, jmax))
      !allocate(S_inv(Nx, jmax))
    !end if
    allocate(S_k(ntx, nty), S_inv(ntx, nty))

    allocate(kx_arr(ntx), ky_arr(nty))

    do ix = 1, ntx
      kx_arr(ix) = getkx(ix)
    end do
    do iy = 1, nty
      ky_arr(iy) = getky(iy)
    end do

    !px = 1.0d0/dble(ntx)  ! assuming len_x = 1 in fractional coordinates
    !py = 1.0d0/dble(nty)  ! assuming len_y = 1 in fractional coordinates
    p2 = px * py

    ! Build full/half S_k in unshifted FFT index order:
    ! index i -> n_x = (i-1) if (i-1)<=Nx/2 else (i-1 - Nx)
    maxS = 0.0d0
    !do i = 1, ntx
    do ix = 1, ntx
      !ix = i - 1
      !if (ix > Nx/2) ix = ix - Nx
      !argx = acos(-1.0_dp) * real(ix, dp) / real(Nx, dp)   ! pi * ix / Nx
      kx = kx_arr(ix)
      !do j = 1, jmax
      do iy = 1, nty
        !iy = j - 1
        !if (.not. half) then
          !if (iy > Ny/2) iy = iy - Ny
        !end if
        !argy = acos(-1.0_dp) * real(iy, dp) / real(Ny, dp)  ! pi * iy / Ny
        ky = ky_arr(iy)
        !ky=1
        tmp = p2 * safe_sinc(kx*px) * safe_sinc(ky*py)
        !print *, 'tmp=', p2
        S_k(ix,iy) = tmp
        if (abs(tmp) > maxS) maxS = abs(tmp)
      end do
    end do

    ! If user didn't supply a reg, pick small fraction of max(S).
    !if (reg < 0.0_dp) reg = maxS * 1.0d-8
    reg = maxS * 1.0d-8 

    ! Build regularized inverse
    select case (trim(adjustl(mth)))
    case ("threshold")
      ! threshold: inverse only where |S| > reg, else zero
      write(*,*) "build_Sk: threshold method."
      do ix = 1, size(S_k,1)
        do iy = 1, size(S_k,2)
          if (abs(S_k(ix,iy)) > reg) then
            S_inv(ix,iy) = 1.0d0 / S_k(ix,iy)
          else
            S_inv(ix,iy) = 0.0d0
          end if
        end do
      end do
    case ("wiener")
      ! Wiener-like: S / (S^2 + reg^2) — smooth suppression near zeros
      write(*,*) "build_Sk: wiener method."
      do ix = 1, size(S_k,1)
        do iy = 1, size(S_k,2)
          S_inv(ix,iy) = S_k(ix,iy) / ( S_k(ix,iy)*S_k(ix,iy) + reg*reg )
        end do
      end do
    !case ("direct")
      ! direct (not recommended): raw inverse with tiny epsilon guard
      !do i = 1, size(S_k,1)
        !do j = 1, size(S_k,2)
          !if (abs(S_k(i,j)) < reg) then
            !S_inv(i,j) = 0.0_dp
          !else
            !S_inv(i,j) = 1.0_dp / S_k(i,j)
          !end if
        !end do
      !end do
    case ("unit")
      ! unit (not recommended): inverse with invS_k -> 1 when small
      write(*,*) "build_Sk: unit method."
      do ix = 1, size(S_k,1)
        do iy = 1, size(S_k,2)
          S_inv(ix,iy) = ( S_k(ix,iy) + reg*reg ) / ( S_k(ix,iy)*S_k(ix,iy) + reg*reg )
        end do
      end do
    case default
      write(*,*) "build_Sk: unknown method. Using wiener."
      do ix = 1, size(S_k,1)
        do iy = 1, size(S_k,2)
          S_inv(ix,iy) = S_k(ix,iy) / ( S_k(ix,iy)*S_k(ix,iy) + reg*reg )
        end do
      end do
    end select

  end subroutine build_Sk

