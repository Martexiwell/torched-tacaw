  subroutine calc_transmission(isl) ! calculate V(x,y) and Az(x,y) and from them the transmission function

    use global, only : nx, ny, nz, nscx, nscy, ntx, nty, coff, sigma, bandlimit, output_array, output_array_real!,debug!,ntz
    use atoms, only  : lat, cube, atpres, z, magm, magv, get_atom_coord, get_cube_indices
    use fdes_const, only  : echarge, rplanck
    !use fftw, only   : bw_mask

    implicit none

    integer, intent(in) :: isl ! slice number

    double precision, parameter :: eps = 1.0d-30

    integer ix, iy, iz, icx, icy, icz, jx, jy, iat, ir, iat_rad, iat_nei
    double precision r_frac(3), r_Ang(3), r2_frac(3), r2_Ang(3), cut2, deltaR(3), dis, magfac!,rx, ry
    
    iz = mod(isl-1,nz)+1             ! in case of supercells, cubation is only available within unit  cell
    r_frac(3) = (dble(iz)-0.5d0)/dble(nz)  ! we pick a fractional z-coordinate in the center of the slice thickness
    !r_frac(3) = (dble(iz))/dble(nz) - 0.5d0  ! we pick a fractional z-coordinate in the center of the slice thickness
    r_Ang(3) = r_frac(3)*lat(3)
    
    cut2 = coff*coff
    magfac = echarge/rplanck*1.d-20 ! this is e/hbar with a conversion Å^2/m^2
    !if(debug) print *, 'Magnetic prefactor (units T^-1 * Å^-2): ', magfac
    
    pot(:,:)=dble(0)
    Az(:,:) =dble(0)
    
    do ix=1, nx
      r_frac(1) = dble(ix-1)/dble(nx)+eps
      r_Ang(1) = r_frac(1)*lat(1)
      do iy=1, ny
        r_frac(2) = dble(iy-1)/dble(ny)+eps
        r_Ang(2) = r_frac(2)*lat(2)
        call get_cube_indices(r_frac,icx,icy,icz)
        do jx=icx-1, icx+1
          do jy=icy-1, icy+1
            do iat=1, cube(jx,jy,icz,0)
              call get_atom_coord(jx,jy,icz,iat,r2_frac)
              r2_Ang(:) = lat(:)*r2_frac(:)
              deltaR = r2_Ang - r_Ang
              dis = sqrt(deltaR(1)*deltaR(1)+deltaR(2)*deltaR(2))
              if(dis<eps) dis=eps
              if(dis<coff) then ! atom iat in cube (jx,jy,icz) is closer than cut-off
                ir = floor(dis/coff*dble(nrad))
                iat_nei = cube(jx,jy,icz,iat)  ! cube(i,j,k,i_at) contains the index of atom (line within structure file)
                iat_rad = atpres(z(iat_nei))   ! z(atom_index) gives its Z-number, and atpres(Z) gives index of radial function for this atom
                pot(ix,iy) =  pot(ix,iy) + (radV(iat_rad,ir)*(r_V(ir+1)-dis)+radV(iat_rad,ir+1)*(dis-r_V(ir)))/(r_V(ir+1)-r_V(ir))
                !if(debug) write(*,'(5i3,7f8.4,2i3,2g10.3)') ix, iy, jx, jy, iat, r2_Ang, r_Ang, dis, ir, iat_rad, pot(ix,iy)
                ! now the magnetic part
                ir = floor(dis/r_Az(nrad_mag)*dble(nrad_mag))
                Az(ix,iy) = Az(ix,iy) + magm(iat_nei) * (magv(iat_nei,1)*deltaR(2)-magv(iat_nei,2)*deltaR(1)) * (radAz(iat_rad,ir)*(r_Az(ir+1)-dis)+radAz(iat_rad,ir+1)*(dis-r_Az(ir)))/(r_Az(ir+1)-r_Az(ir))
                !if(debug) write(*,'(5i4,7f8.4,2i6,g10.3)') ix, iy, jx, jy, iat, magv(iat,:), deltaR, dis, ir, iat_rad, Az(ix,iy)
              endif
            enddo
          enddo
        enddo
      enddo
    enddo

    ! we need to prepare bandwidth-limiting of the potential and Az
    ! -> so we first expand them to the full supercell size (if needed)
    if(nscx>1) then
      do ix=2, nscx
        pot( (ix-1)*nx+1 : ix*nx, 1:ny) = pot(1:nx,1:ny)
         Az( (ix-1)*nx+1 : ix*nx, 1:ny) =  Az(1:nx,1:ny)
      enddo
    endif
    if(nscy>1) then
      do iy=2, nscy
        pot(:, (iy-1)*ny+1 : iy*ny) = pot(:,1:ny)
        Az(:, (iy-1)*ny+1 : iy*ny) =  Az(:,1:ny)
      enddo
    endif

    ! now comes the BW limiting of both potentials (pot, Az) --> need to do that for Az also!! (or add it to pot?)
    !magfac=0.d0 ! this can be used to switch off magnetism (temporary solution)
    pot = pot - magfac/sigma*Az
    if(bandlimit) call bw_limit(pot)

    ! now we can calculate the transmission function ---> can we do trans = exp( cmplx(0.d0,1.d0)*(sigma*pot-magfac*Az) ) ?
    do ix=1,ntx
      do iy=1,nty
        trans(ix,iy) = exp( dcmplx(0.d0,1.d0)*sigma*pot(ix,iy) )
      enddo
    enddo

    ! and BW-limit that as well
    if(bandlimit) call bw_limit(trans)

    !if(isl==nz) call output_array(pot,'(a,i0,a)','potential_',isl,'.dat')
    !if(isl==nz) call output_array_real(Az,'(a,i0,a)','Az_',isl,'.dat')
    !if(isl==nz) call output_array(trans,'(a,i0,a)','T_',isl,'.dat')

  end subroutine calc_transmission

