  ! safe sinc: sin(x)/x with small-x expansion
  !--------------------------------------------------------------------------------------------------------------------------
  pure function safe_sinc(x) result(s)

    use fdes_const, only  : pi

    double precision, intent(in) :: x
    double precision :: s
    double precision, parameter :: eps = 1.0d-12

    if (abs(x) < eps) then
      ! Taylor expansion: sin(x)/x ~ 1 - pi*x*pi*x/6.0_dp + ...
      s = 1.0d0 - pi*x*pi*x/6.0d0
    else
      s = sin(pi*x) / (pi*x)
    end if
  end function safe_sinc

  ! Build S_k and regularized inverse S_inv
